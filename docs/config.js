// Realtime Connection Manager (Supabase Realtime) - FIXED VERSION
class RealtimeManager {
    constructor() {
        this.connected = false;
        this.sessionId = null;
        this.userType = null;
        this.userId = null;
        this.listeners = new Map();
        this.subscriptions = [];
        this.supabase = null;
    }
    
    connect(sessionId, userType, userId) {
        this.sessionId = sessionId;
        this.userType = userType;
        this.userId = userId;
        
        if (!supabaseClient) {
            console.error('‚ùå Supabase client not initialized');
            return;
        }
        
        this.supabase = supabaseClient;
        
        try {
            console.log('üîó Connecting to Supabase Realtime - MathsQuizzv2');
            console.log('üë§ User:', this.userType, this.userId);
            
            // √âcouter les √©v√©nements de quiz
            this.subscribeToQuizEvents();
            
            // √âcouter les changements de participants
            this.subscribeToParticipants();
            
            // √âcouter les changements de session
            this.subscribeToSession();
            
            this.connected = true;
            this.emit('connected');
            
            console.log('‚úÖ Supabase Realtime connected');
            
        } catch (error) {
            console.error('‚ùå Failed to connect to Supabase Realtime:', error);
            this.emit('error', error);
        }
    }
    
    subscribeToQuizEvents() {
        const eventsSubscription = this.supabase
            .channel('quiz_events_' + this.sessionId)
            .on('postgres_changes', 
                { 
                    event: 'INSERT', 
                    schema: 'public', 
                    table: 'quiz_events',
                    filter: `session_id=eq.${this.sessionId}`
                }, 
                (payload) => {
                    this.handleQuizEvent(payload.new);
                }
            )
            .subscribe();
            
        this.subscriptions.push(eventsSubscription);
        console.log('üéÆ Subscribed to quiz events for session:', this.sessionId);
    }
    
    subscribeToParticipants() {
        const participantsSubscription = this.supabase
            .channel('participants_' + this.sessionId)
            .on('postgres_changes', 
                { 
                    event: '*', 
                    schema: 'public', 
                    table: 'participants',
                    filter: `session_id=eq.${this.sessionId}`
                }, 
                (payload) => {
                    this.handleParticipantChange(payload);
                }
            )
            .subscribe();
            
        this.subscriptions.push(participantsSubscription);
        console.log('üë• Subscribed to participants changes');
    }
    
    subscribeToSession() {
        const sessionSubscription = this.supabase
            .channel('session_' + this.sessionId)
            .on('postgres_changes', 
                { 
                    event: 'UPDATE', 
                    schema: 'public', 
                    table: 'sessions',
                    filter: `id=eq.${this.sessionId}`
                }, 
                (payload) => {
                    this.handleSessionChange(payload.new);
                }
            )
            .subscribe();
            
        this.subscriptions.push(sessionSubscription);
        console.log('üéØ Subscribed to session changes');
    }
    
    handleQuizEvent(event) {
        console.log('üì® Quiz event received:', event.event_type, 'target:', event.target_type, 'userType:', this.userType);
        
        // V√©rifier si l'√©v√©nement nous concerne
        const shouldHandle = (
            event.target_type === 'all' || 
            event.target_type === this.userType ||
            (event.target_type === 'specific' && event.target_user_id === this.userId)
        );
        
        console.log('üéØ Should handle event:', shouldHandle);
        
        if (shouldHandle) {
            console.log('‚úÖ Processing event:', event.event_type);
            // √âmettre l'√©v√©nement avec ses donn√©es
            this.emit(event.event_type, event.event_data);
            this.emit('message', { type: event.event_type, ...event.event_data });
        } else {
            console.log('‚ùå Event not for us - target:', event.target_type, 'our type:', this.userType);
        }
    }
    
    handleParticipantChange(payload) {
        console.log('üë§ Participant change:', payload.eventType);
        
        if (payload.eventType === 'INSERT') {
            this.emit('user_joined', { 
                userType: 'student', 
                userId: payload.new.id,
                studentName: payload.new.student_name 
            });
        } else if (payload.eventType === 'UPDATE') {
            this.emit('participant_updated', payload.new);
        } else if (payload.eventType === 'DELETE') {
            this.emit('user_left', { 
                userType: 'student', 
                userId: payload.old.id 
            });
        }
    }
    
    handleSessionChange(session) {
        console.log('üéØ Session change:', session.status);
        this.emit('session_updated', session);
    }
    
    async send(data) {
        if (!this.connected || !this.supabase) {
            console.warn('Realtime not connected, message not sent:', data);
            return;
        }
        
        try {
            // D√©terminer le type de cible selon le type de message
            let targetType = 'all';
            let targetUserId = null;
            
            if (data.type === 'student_answer' && this.userType === 'student') {
                targetType = 'teacher';
                console.log('üéØ Student answer -> targeting teachers');
            } else if (['start_quiz', 'next_question', 'quiz_finished'].includes(data.type) && this.userType === 'teacher') {
                targetType = 'student';
                console.log('üéØ Teacher command -> targeting students');
            } else if (data.type === 'answer_confirmed') {
                targetType = 'specific';
                targetUserId = this.userId;
                console.log('üéØ Answer confirmation -> targeting specific student');
            } else {
                console.log('üéØ Default targeting -> all users');
            }
            
            console.log('üì§ Sending event:', data.type, 'to target:', targetType);
            
            // Ins√©rer l'√©v√©nement dans la table quiz_events
            const { data: insertedEvent, error } = await this.supabase
                .from('quiz_events')
                .insert({
                    session_id: this.sessionId,
                    event_type: data.type,
                    event_data: data,
                    target_type: targetType,
                    target_user_id: targetUserId
                })
                .select()
                .single();
                
            if (error) {
                console.error('‚ùå Error sending realtime event:', error);
            } else {
                console.log('üì§ Realtime event sent:', data.type, 'inserted with ID:', insertedEvent.id);
            }
            
        } catch (error) {
            console.error('‚ùå Error in realtime send:', error);
        }
    }
    
    on(event, callback) {
        if (!this.listeners.has(event)) {
            this.listeners.set(event, []);
        }
        this.listeners.get(event).push(callback);
    }
    
    off(event, callback) {
        if (this.listeners.has(event)) {
            const callbacks = this.listeners.get(event);
            const index = callbacks.indexOf(callback);
            if (index > -1) {
                callbacks.splice(index, 1);
            }
        }
    }
    
    emit(event, data) {
        if (this.listeners.has(event)) {
            this.listeners.get(event).forEach(callback => {
                try {
                    callback(data);
                } catch (error) {
                    console.error(`Error in ${event} listener:`, error);
                }
            });
        }
    }
    
    disconnect() {
        console.log('üìµ Disconnecting Supabase Realtime');
        
        // Unsubscribe from all channels
        this.subscriptions.forEach(subscription => {
            this.supabase.removeChannel(subscription);
        });
        
        this.subscriptions = [];
        this.connected = false;
        this.listeners.clear();
    }
}

